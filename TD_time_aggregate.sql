SELECT $TD_TIMECODE_RANGE
	, page
	, COUNT(*) 
FROM retail_to_sessionize 
GROUP BY TIME (MINUTES(30) AND customerid,page) 
USING TIMECODE(tstamp)  ;

SELECT $TD_TIMECODE_RANGE
	, page
	, COUNT(*) 
FROM retail_to_sessionize 
GROUP BY TIME (MINUTES(30) AND customerid,page) 
USING TIMECODE(tstamp)  WHERE tstamp 
	BETWEEN TIMESTAMP '2017-01-03 14:57:50.000000'
	AND TIMESTAMP '2017-01-03 15:31:26.000000' ;

	
SELECT $TD_TIMECODE_RANGE
	, COUNT(*) 
FROM retail_to_sessionize 
GROUP BY TIME (DAYS(30)) 
USING TIMECODE(tstamp)  ;
	
SELECT $TD_TIMECODE_RANGE
	, COUNT(*) 
FROM retail_to_sessionize 
GROUP BY TIME (DAYS(15)) 
USING TIMECODE(tstamp)  
WHERE tstamp 
	>= TIMESTAMP '2017-01-01 00:00:00.000000';	
	
	
